৬. অপরিচিত হলে তাদের শনাক্তকরণের বর্ণনা দিতে হবে।<br>
৭. জিডি নথিভুক্ত করে বিষয়টি সুষ্ঠু তদন্তপূর্বক আইনানুগ ব্যবস্থা গ্রহণের জন্য আবেদন করতে হবে।<br>
