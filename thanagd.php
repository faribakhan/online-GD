নানাবিধ সমস্যায় পড়ে আমরা আইনশৃঙ্খলাবাহিনীর দ্বারস্থ হয়ে থাকি। কিন্তু কোনো অনাকাঙ্ক্ষিত ঘটনা ঘটার আশঙ্কা থাকলে তা থানায় লিখিত আকারে জানালে পুলিশ এ ব্যাপারে যথাযথ পদক্ষেপ নিতে পারে। আর কিভাবে আমরা থানা থেকে সেই সহযোগিতা নিতে পারি তা অনেকেরই জানা নেই। <br><br>

মূল্যবান যে কোনো জিনিস হারালে যেমন- সার্টিফিকেট, দলিল, লাইসেন্স, পাসপোর্ট, মূল্যবান রশিদ, চেকবই, এটিএম বা ক্রেডিট স্বর্ণালংকার, নগদ অর্থ ইত্যাদি। অথবা কোনো প্রকার হুমকি পেলে বা হুমকির আশংকা থাকলে কিংবা কেউ নিখোঁজ হলেও জিডি করার সুযোগ রয়েছে। অর্থাৎ সাধারণত যেসব ক্ষেত্রে মামলা হয় না সেসব ক্ষেত্রেই থানায় জিডি করা যায়। জিডি করার কোন বাধ্যবাধকতা না থাকলেও নতুন করে হারানো কাগজ তুলতে হলেও জিডির কপির প্রয়োজন হয়।<br><br>

এসব ক্ষেত্রে আপনার প্রথম কাজ হলো নিকটবর্তী থানাকে জানানো। আর এটি জানাবেন একটি সাধারণ ডায়েরির মাধ্যমে, যাকে আমরা সংক্ষেপে জিডি বলে থাকি। এ ধরনের জিডি করার অর্থ হলো- বিষয়টি সম্পর্কে থানাকে অবগত করা, যাতে থানা কর্তৃপক্ষ সম্ভাব্য অপরাধটি সংঘটিত হওয়ার আগেই ব্যবস্থা নিতে পারে।<br><br>

