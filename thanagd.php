৩. অপরাধ সংঘটিত হওয়ার আশঙ্কা করলে জিডিতে আশঙ্কার কারণ উল্লেখ করতে হবে।<br>
৪. হুমকি দিলে হুমকি দেওয়ার স্থান, তারিখ, সময়, সাক্ষী থাকলে তাদের নাম, পিতার নাম ও পূর্ণ ঠিকানা উল্লেখ করতে হবে।<br>
